    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setAcceptAllFileFilterUsed(false);
        fc.addChoosableFileFilter(new FileNameExtensionFilter("Project JSON Files", "project.json", "txt", "json"));
        int returnVal = fc.showSaveDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String name = fc.getSelectedFile().getAbsolutePath();
            String realname = (name.substring(name.length() - "-project.json".length(), name.length()).toLowerCase().equals("-project.json")) ? name : fc.getSelectedFile().getAbsolutePath() + "-project.json";
            jf = new JSONUI_File(realname);
            jf.setJSON(jTextArea2.getText());
            jf.saveFile();
            JOptionPane.showMessageDialog(this, "<html>Project Saved<br>" + jf.getJSON_File().getAbsolutePath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

