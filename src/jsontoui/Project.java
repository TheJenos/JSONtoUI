    public void updateCombobox() {
        //jComboBox1.removeAllItems();
        for (int i = 0; i < dtm.getChildCount(UI); i++) {
            DefaultMutableTreeNode f1 = (DefaultMutableTreeNode) dtm.getChild(UI, i);
            FileObject f = (FileObject) f1.getUserObject();
            jComboBox1.addItem(f.toString());
        }
    }

    public void updateJSON() {
        try {
            String script_lines[] = jTextArea1.getText().split("\n");
            for (String script_line : script_lines) {
                String key = script_line.split("=")[0];
                String value = script_line.split("=")[1];
                sat.put(key, value);
            }
        } catch (Exception e) {
        }
        JSONObject Uis = new JSONObject();
        for (int i = 0; i < dtm.getChildCount(UI); i++) {
            DefaultMutableTreeNode f1 = (DefaultMutableTreeNode) dtm.getChild(UI, i);
            FileObject f = (FileObject) f1.getUserObject();
            Uis.put(f.toString(), f.getFile());
            dtm.nodeChanged(UI);
        }
        JSONObject scrpts = new JSONObject();
        for (int i = 0; i < dtm.getChildCount(Scripts); i++) {
            DefaultMutableTreeNode f1 = (DefaultMutableTreeNode) dtm.getChild(Scripts, i);
            FileObject f = (FileObject) f1.getUserObject();
            scrpts.put(f.toString(), f.getFile());
            dtm.nodeChanged(Scripts);
        }
        JSONObject jo = new JSONObject();
        jo.put("Project Name", jTextField3.getText());
        jo.put("Static Values", sat);
        jo.put("Type", "project");
        jo.put("Host", jTextField1.getText());
        jo.put("Port", jTextField2.getText());
        jo.put("Root User", jTextField4.getText());
        jo.put("Root Password", jTextField5.getText());
        jo.put("DataBase Name", jTextField6.getText());
        jo.put("Main UI", jComboBox1.getSelectedItem());
        jo.put("UI", Uis);
        jo.put("Scripts", scrpts);
        jTextArea2.setText(jo.toString(4));
    }

    public void updateFromJSON() {
        JSONObject jo = new JSONObject(jf.getJSON());
        jTextField3.setText(jo.get("Project Name").toString());
        project.setUserObject(jTextField3.getText() + " (Project)");
        dtm.nodeChanged(project);
        sat = jo.getJSONObject("Static Values");
        String satics = "";
        HashMap<String, Object> m = (HashMap<String, Object>) sat.toMap();
        for (Map.Entry<String, Object> entry : m.entrySet()) {
            String key = entry.getKey();
            Object value = entry.getValue();
            satics += key + "=" + value + "\n";
        }
        jTextArea1.setText(satics);
        jTextField1.setText(jo.get("Host").toString());
        jTextField2.setText(jo.get("Port").toString());
        jTextField4.setText(jo.get("Root User").toString());
        jTextField5.setText(jo.get("Root Password").toString());
        jTextField6.setText(jo.get("DataBase Name").toString());
        JSONObject Uis = jo.getJSONObject("UI");
        HashMap<String, Object> m1 = (HashMap<String, Object>) Uis.toMap();
        UI.removeAllChildren();
        dtm.nodeChanged(UI);
        dtm.reload();
        for (Map.Entry<String, Object> entry : m1.entrySet()) {
            String key = entry.getKey();
            Object value = entry.getValue();
            UI.add(new DefaultMutableTreeNode(new FileObject(new File(value.toString()), key)));
            dtm.nodeChanged(UI);
            dtm.reload();
        }
        updateCombobox();
        jComboBox1.setSelectedItem(jo.get("Main UI").toString());
        JSONObject scipts = jo.getJSONObject("Scripts");
        HashMap<String, Object> m2 = (HashMap<String, Object>) scipts.toMap();
        Scripts.removeAllChildren();
        dtm.nodeChanged(Scripts);
        dtm.reload();
        for (Map.Entry<String, Object> entry : m2.entrySet()) {
            String key = entry.getKey();
            Object value = entry.getValue();
            Scripts.add(new DefaultMutableTreeNode(new FileObject(new File(value.toString()), key)));
            dtm.nodeChanged(Scripts);
            dtm.reload();
        }
    }

